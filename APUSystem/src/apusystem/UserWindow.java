    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileWriter writer = null;
        try {
            File file = new File("orders.txt");
            writer = new FileWriter(file, true);
            if (Integer.parseInt(jTextField1.getText()) >= price) {
                writer.write(LoginWindow.currentUserName + "," + jComboBox1.getSelectedItem().toString() + "," + jTextField1.getText() + "," + jTextField2.getText() + "\n");
                JOptionPane.showMessageDialog(this, "Order Added");
                jTextField1.setText("");
                jTextField2.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Not enough amount entered to complete the order!");
            }
        } catch (IOException ex) {
            Logger.getLogger(UserWindow.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                writer.close();
            } catch (IOException ex) {
                Logger.getLogger(UserWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

