    public UserWindow() {
        try {
            initComponents();
            jComboBox1.removeAllItems();
            products.clear();
            File file = new File("products.txt");
            Scanner sc = new Scanner(file);
            while (sc.hasNextLine()) {
                String line = sc.nextLine();
                String[] lineParts = line.split(",");
                products.add(new Product(lineParts[0], Integer.parseInt(lineParts[1])));
                jComboBox1.addItem(lineParts[0]);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(UserWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        jComboBox1.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                for (Product product : products) {
                    if (product.getName().equalsIgnoreCase(jComboBox1.getSelectedItem().toString())) {
                        jLabel2.setText("You must pay: " + product.getAmount());
                        price = product.getAmount();
                    }
                }
            }
        });
    }

