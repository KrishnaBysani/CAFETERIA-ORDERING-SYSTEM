    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileWriter writer = null;
        try {
            File file = new File("users.txt");
            writer = new FileWriter(file, true);
            if (jTextField1.getText().equals("") || jPasswordField1.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter valid data...");
            } else {
                writer.write(jTextField1.getText() + "," + jPasswordField1.getText() + "\n");
                JOptionPane.showMessageDialog(null, "Customer Added!");
                LoginWindow ob = new LoginWindow();
                ob.setVisible(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(RegisterCustomerWindow.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                writer.close();
            } catch (IOException ex) {
                Logger.getLogger(RegisterCustomerWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

